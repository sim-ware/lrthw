# Q: Write at least one more function of your own design, and run it 10
#    different ways.


def pens_and_pencils(pens, pencils)
  puts "You have #{pens} pens!"
  puts "You have #{pencils} pencils!"
  puts "Get back to work.\n"
end

#1
pens_and_pencils(12, 21)
